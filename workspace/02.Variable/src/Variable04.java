public class Variable04 {
	public static void main(String[] args) {
		//실수형 데이터 타입 : float(4byte), double(8byte)
		float num1 = 123.4567890123456789F; //값을 할당할 때 반드시 접미사로 F(f)를 붙인다.
		double num2 = 123.4567890123456789; 
		
		System.out.println("변수 num1의 값 : "+num1);
		System.out.println("변수 num2의 값 : "+num2);
	}//main()
}//class
